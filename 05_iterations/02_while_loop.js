// While loop in JavaScript

// while loop
let i = 1;

while (i <= 10) {
  console.log(i);
  i++;
}

// looping an array using while loop
fruits = ["mango", "Apple", "Banana", "Guava"];
let j = 0;

while (j < fruits.length) {
  console.log(fruits[j]);
  j++;
}

// do-while loop
let score = 1;

do {
  console.log(`Score is: ${score}`);
  score++;
} while (score <= 10);
